## 계별 대표값 합친 후 정리
계별대표값 <- bind_rows(
  생활계_인구_4합계, 생활계_물사용량_4합계, 축산계_4합계_2,
  산업계_4합계_2_폐수방류, 토지계_4합계_2
) %>%
  select(-c(권역, `2014`:`2018`)) %>%
  mutate(
    분류 = factor(분류, levels = c(
      "인구", "물사용량", "소", "돼지", "가금", "폐수방류량", "농경지"
    )),
    # CAGR(Compound Annual Growth Rate, 연평균 증가율) 계산
    # CAGR = [마지막 연도 값 / 처음 연도 값] ^ 1/(마지막 연도 - 처음 연도) - 1
    CAGR = round(((`2023` / `2019`)^(1 / (2023 - 2019)) - 1) * 100, 2),
    # inf(무한대, 1/0 또는 -1/0 인 경우), NaN(0/0처럼 수학적으로 정의 되지 않은 경우) 처리
    CAGR = case_when(
      is.na(CAGR) ~ 0,
      is.infinite(CAGR) ~ 0,
      .default = CAGR
    ),
    증가율_19_23 = round((`2023` - `2019`) / `2019` * 100, 2),
    증가율_19_23 = case_when(
      is.na(증가율_19_23) ~ 0,
      is.infinite(증가율_19_23) ~ 0,
      .default = 증가율_19_23
    )
  ) %>%
  arrange(단위유역, 분류, 시군)



